def my_re(line):
    result_list = list()
    for index in range(1, len(line) - 1):
        if line[index].islower() and line[index - 1].isupper() and line[index + 1].isupper():
            result_list.append(line[index])
    return result_list

# 2ой способ

def with_re(line):
    result_list = list()
    patern = '(?=([A-Z]{1}[a-z]{1}[A-Z]{1}))'
    result = re.findall(patern, line)
    for string in result:
        result_list.append(re.findall('[a-z]+', string)[0])
    return result_list

